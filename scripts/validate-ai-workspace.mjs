#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';

const repoRoot = process.cwd();

const requiredPaths = [
  '.ai',
  '.ai/registry.yml',
  '.ai/backlog.yml',
  '.ai/handoffs',
  '.ai/iddp',
  '.ai/initiatives/active',
  '.ai/initiatives/planning',
  '.ai/initiatives/complete',
  '.ai/initiatives/archive',
  'ROADMAP.md',
  'CLAUDE.md',
];

const forbiddenPaths = [
  'docs/session-state.yaml',
  'docs/active-initiatives.yaml',
  'docs/sessions',
  'docs/guidance-updates',
  'docs/plans',
  '.ai/project.yaml',
  '.ai/context',
  '.ai/data-model',
  '.ai/debug',
  '.ai/decisions',
  '.ai/feedback',
  '.ai/refinement',
  '.ai/reviews',
  '.ai/sessions',
  '.ai/specs',
  '.ai/tasks',
  'project-profile.yaml',
  'FIRST-SESSION-PROMPT.md',
  'docs/.iddp',
  'docs/bootstrap-questionnaire.md',
  'docs/bootstrap-state.yaml',
  'docs/PROJECT_STATE.md',
  'docs/TASKS.md',
];

const forbiddenContentPatterns = [
  { pattern: /docs\/session-state\.yaml/g, label: 'docs/session-state.yaml' },
  { pattern: /docs\/active-initiatives\.yaml/g, label: 'docs/active-initiatives.yaml' },
  { pattern: /docs\/sessions\//g, label: 'docs/sessions/' },
  { pattern: /docs\/guidance-updates\//g, label: 'docs/guidance-updates/' },
  { pattern: /docs\/plans\//g, label: 'docs/plans/' },
  { pattern: /\.ai\/tasks\/backlog\.yaml/g, label: '.ai/tasks/backlog.yaml' },
  { pattern: /\.ai\/project\.yaml/g, label: '.ai/project.yaml' },
  { pattern: /project-profile\.yaml/g, label: 'project-profile.yaml' },
  { pattern: /FIRST-SESSION-PROMPT\.md/g, label: 'FIRST-SESSION-PROMPT.md' },
  { pattern: /Generated by \[ai-dev-system\]/g, label: 'ai-dev-system footer' },
];

const contentScanExcludes = new Set([
  'scripts/validate-ai-workspace.mjs',
  '.githooks/pre-commit',
]);

const errors = [];

for (const relativePath of requiredPaths) {
  if (!fs.existsSync(path.join(repoRoot, relativePath))) {
    errors.push(`Missing required path: ${relativePath}`);
  }
}

for (const relativePath of forbiddenPaths) {
  if (fs.existsSync(path.join(repoRoot, relativePath))) {
    errors.push(`Forbidden legacy path still exists: ${relativePath}`);
  }
}

const walk = (relativeDir) => {
  const absoluteDir = path.join(repoRoot, relativeDir);
  const entries = fs.readdirSync(absoluteDir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const relativePath = path.join(relativeDir, entry.name);
    if (entry.isDirectory()) {
      if (['.git', 'node_modules', 'dist', 'build', 'coverage'].includes(entry.name)) {
        continue;
      }
      files.push(...walk(relativePath));
      continue;
    }
    files.push(relativePath.replace(/\\/g, '/'));
  }

  return files;
};

const trackedFiles = walk('.');

for (const relativePath of trackedFiles) {
  const normalizedPath = relativePath.replace(/^\.\//, '');
  if (contentScanExcludes.has(normalizedPath)) {
    continue;
  }

  const absolutePath = path.join(repoRoot, normalizedPath);
  let content;
  try {
    content = fs.readFileSync(absolutePath, 'utf8');
  } catch {
    continue;
  }

  if (content.includes('\u0000')) {
    continue;
  }

  for (const { pattern, label } of forbiddenContentPatterns) {
    pattern.lastIndex = 0;
    if (pattern.test(content)) {
      errors.push(`Forbidden legacy reference "${label}" found in ${normalizedPath}`);
    }
  }
}

const registryPath = path.join(repoRoot, '.ai/registry.yml');
if (fs.existsSync(registryPath)) {
  const registryContent = fs.readFileSync(registryPath, 'utf8');
  const registryPaths = [...registryContent.matchAll(/^\s*path:\s+"([^"]+)"/gm)].map((match) => match[1]);
  for (const relativePath of registryPaths) {
    const initiativePath = path.join(repoRoot, relativePath);
    if (!fs.existsSync(initiativePath)) {
      errors.push(`Registry path does not exist: ${relativePath}`);
      continue;
    }
    if (!fs.existsSync(path.join(initiativePath, 'plan.md')) && !fs.existsSync(path.join(initiativePath, 'guidance.md'))) {
      errors.push(`Registry path has no plan.md or guidance.md: ${relativePath}`);
    }
  }
}

const roadmapPath = path.join(repoRoot, 'ROADMAP.md');
if (fs.existsSync(roadmapPath)) {
  const roadmapContent = fs.readFileSync(roadmapPath, 'utf8');
  const roadmapRefs = [...roadmapContent.matchAll(/\.ai\/initiatives\/[A-Za-z0-9\-\/]+\/(?:plan|guidance)\.md/g)].map((match) => match[0]);
  for (const relativePath of roadmapRefs) {
    if (!fs.existsSync(path.join(repoRoot, relativePath))) {
      errors.push(`ROADMAP.md references a missing initiative file: ${relativePath}`);
    }
  }
}

if (errors.length > 0) {
  console.error('AI workspace validation failed:\n');
  for (const error of errors) {
    console.error(`- ${error}`);
  }
  process.exit(1);
}

console.log('AI workspace validation passed.');
